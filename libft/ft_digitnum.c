#include "libft.h"

int		ft_digitnum(long int n)
{
	size_t	i;

	i = 0;
	if (!n && n != 0)
		return (0);
	if (n == 0)
		return (1);
	while (n / 10 != 0)
	{
		n /= 10;
		i++;
	}
	return (i + 1);
}
