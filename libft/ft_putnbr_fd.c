#include <unistd.h>
#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char		c;
	long int	n1;

	n1 = n;
	if (n1 < 0)
	{
		write(fd, "-", 1);
		n1 *= -1;
	}
	if (n1 > 9)
		ft_putnbr_fd(n1 / 10, fd);
	c = n1 % 10 + '0';
	write(fd, &c, 1);
}
