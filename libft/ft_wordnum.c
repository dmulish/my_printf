#include "libft.h"

size_t	ft_wordnum(char *s, char c)
{
	int		i;
	size_t	num;

	i = -1;
	num = 0;
	if (!s)
		return (0);
	while (s[++i])
	{
		if (s[i] != c && (s[i + 1] == c || s[i + 1] == '\0'))
			num++;
	}
	return (num);
}
