#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	unsigned char	*s1;

	s1 = (unsigned char*)s;
	while (*s1 || c == '\0')
	{
		if (*s1 == c)
			return ((char*)s1);
		s1++;
	}
	return (0);
}
